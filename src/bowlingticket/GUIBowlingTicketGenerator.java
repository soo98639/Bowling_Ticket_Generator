    private void generateTicketBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateTicketBtnActionPerformed
        try
        {
            String ticketNo = ticketTF.getText();
            String custName = nameTF.getText();
            String playTime = null;
            if(pHolidayRB.isSelected())
            {
                playTime = "Public Holiday";
            }else{}
            
            if(oDayRB.isSelected())
            {
                playTime = "Ordinary Day";
            }else{}
            String playerType = String.valueOf(pTypeCB.getSelectedItem());
            int noOfPlayer = Integer.parseInt(noPlayerTF.getText());
            BowlingTicket2 b1 = new BowlingTicket2(ticketNo, custName, playTime, playerType, noOfPlayer);
            bowlingTicketArrayList.add(b1);
            b1.calculateCharge();
            b1.calculateRebate();
            outputTA.setText(b1.toString());
            jOptionPane1.showMessageDialog(null, "Data is saved.", "Information", jOptionPane1.INFORMATION_MESSAGE);
        }

        catch(Exception e)
        {
            jOptionPane1.showMessageDialog(null, "Please ensure your input is correct!", "Error", jOptionPane1.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_generateTicketBtnActionPerformed

