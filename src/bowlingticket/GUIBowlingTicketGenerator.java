    private void displayAllTicketBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayAllTicketBtnActionPerformed
        outputTA.setText("");
        for(BowlingTicket2 b: bowlingTicketArrayList)
        {
            outputTA.append("<<<TICKET " + (bowlingTicketArrayList.indexOf(b)+1) + ">>>\n");
            b.calculateCharge();;
            b.calculateRebate();
            outputTA.append(b.toString());
        }
    }//GEN-LAST:event_displayAllTicketBtnActionPerformed

    private void displayTotalSalesBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayTotalSalesBtnActionPerformed
        double totalPayment = 0;
        outputTA.setText("TOTAL SALES AS FOR " + dateL.getText() + "\n");
        for(BowlingTicket2 b: bowlingTicketArrayList)
        {
            b.calculateCharge();
            b.calculateRebate();
            totalPayment = totalPayment + b.getTotalPayment();
        }
        outputTA.append(String.format("RM%.2f", totalPayment) + "\n");
    }//GEN-LAST:event_displayTotalSalesBtnActionPerformed

